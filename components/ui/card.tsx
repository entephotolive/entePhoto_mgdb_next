import * as React from "react";
import { cn } from "@/lib/utils/cn";

export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { variant?: "default" | "glass" }
>(({ className, variant = "default", ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border border-white/10 text-white shadow-sm transition-all duration-300",
      variant === "default" && "bg-surface shadow-md",
      variant === "glass" && "bg-white/[0.03] backdrop-blur-xl border-white/[0.08] shadow-glass",
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

